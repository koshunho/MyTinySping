package com.huang.tinyioc;

public class BeanReference {
    private String name;

    private Object bean;
    //一个参数的构造器
    public BeanReference(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Object getBean() {
        return bean;
    }

    public void setBean(Object bean) {
        this.bean = bean;
    }
}
